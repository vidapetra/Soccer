package soccer;

public class Player {

	String name;
	int pace;
	int shooting;
	int passing;
	int dribbling;
	int defence;
	int physical;
	int reflex;
	
	public Player(String name, int pace, int shooting, int passing, 
			int dribbling, int defence, int physical, int reflex) {
		this.name = name;
		this.pace = pace;
		this.shooting = shooting;
		this.passing = passing;
		this.dribbling = dribbling;
		this.defence = defence;
		this.physical = physical;
		this.reflex = reflex;
	}
	
	
}
